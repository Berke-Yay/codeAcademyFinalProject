//Change language of the site
    function updateLanguage(lang){
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.dataset.i18n;
            el.innerHTML = translations[lang][key];
        });
        document.querySelector(".dropbtn img").src = lang=="tr"? "assets/turkey.png" : "assets/united-kingdom.png";
    }

    document.querySelectorAll('[data-lang]').forEach(btn => {
        btn.addEventListener('click', () => {
            const lang = btn.dataset.lang;
            localStorage.setItem('lang', lang);
            updateLanguage(lang);
            createNavbarDropdown();
        });
    });

    const savedLang = localStorage.getItem('lang') || 'tr';
    updateLanguage(savedLang);

//Dropdown Button in the navbar
    function createNavbarDropdown() {
        document.getElementById("navbar-dropdown").classList.toggle("show");
    }

//Mobile Navbar
    const hamburger = document.querySelector('.hamburger');
    const navList = document.querySelector('.navbar-list');

    hamburger.addEventListener('click', () => {
        hamburger.classList.toggle('active');
        navList.classList.toggle('active');
    });

    document.addEventListener('click', (e) => {
        if (!hamburger.contains(e.target) && !navList.contains(e.target)) {
            hamburger.classList.remove('active');
            navList.classList.remove('active');
        }
    });

//Theme button (light/dark mode) in the navbar
    const toggle = document.getElementById("theme-icon");

    if (localStorage.getItem("theme") === "dark") {
        document.documentElement.setAttribute("data-theme", "dark");
        toggle.classList.remove("fa-sun");
        toggle.classList.add("fa-moon");
        toggle.style.padding = "8px 10px";
    }

    toggle.addEventListener("click", () => {
        const currentTheme = document.documentElement.getAttribute("data-theme");
    
        if (currentTheme === "dark") {
            document.documentElement.removeAttribute("data-theme");
            localStorage.setItem("theme", "light");
            toggle.classList.remove("fa-moon");
            toggle.classList.add("fa-sun");
            toggle.style.padding = "8px";
        } else {
            document.documentElement.setAttribute("data-theme", "dark");
            localStorage.setItem("theme", "dark");
            toggle.classList.remove("fa-sun");
            toggle.classList.add("fa-moon");
            toggle.style.padding = "8px 10px";
        }
    });